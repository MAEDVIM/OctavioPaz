               <p>El plan de estudios, está constituido por 37 espacios curriculares, de los cuales, 32 son cursos obligatorios distribuidos las siguientes tres áreas de formación:</p>
               <li>Formación Inicial en Ciencias Sociales.</li>
               <li>Formación Profesional Básica en Educación. </li>
               <li>Líneas específicas mencionadas anteriormente.</li>
               <li>Se incluyen además, cinco cursos optativos que se ofertan de acuerdo a las necesidades de los alumnos y posibilidades de la Unidad.</li>
               <li>Comprende, igualmente, el cumplimiento de 180 horas de prácticas profesionales durante los semestres: 6º. 7º. y 8º.,  así como la prestación de servicio social en instituciones públicas, durante los dos últimos semestres.</li>

