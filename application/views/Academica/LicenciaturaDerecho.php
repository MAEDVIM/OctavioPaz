               <h2 class="text-center">OBJETIVOS</h2>
               <p>La Licenciatura en Intervención Educativa, tiene como objetivo: formar un profesional de la educación capaz de desempeñarse en diversos campos del ámbito educativo, a través de la adquisición de las competencias generales (propias de cualquier profesional del campo de la educación), que le permitan transformar la realidad educativa por medio de los procesos de intervención. </p>
            </div>
            
            <div id="Lista3" class="Elementos">
               <h2 class="text-center">PERFIL DE EGRESO</h2>
				<li>Explicará la noción universal del Derecho y los conceptos jurídicos
				fundamentales.</li>
				<li>Interpretará los principios generales de la normatividad positiva del país.</li>
				<li>Sintetizará al Derecho en sus fuentes y en sus ramas.</li>
				<li>Analizará la problemática socioeconómica del país que influye en la aplicación del
				Derecho.</li>
				<li>Verificará la eficacia de la norma, evaluándola críticamente y situándola en un
				contexto dinámico para que se pueda proponer su modificación si es necesario.</li>
				<li>Distinguirá las diferentes corrientes teóricas en la concepción y aplicación de las
				normas jurídicas.</li>
				<li>Identificará los recursos y necesidades nacionales que son sujetos del estudio
				jurídico y que requieren de alternativas legales para su mejor aprovechamiento.</li>
				<li>Analizará situaciones económicas o políticas desde la perspectiva del Derecho y
				diagnosticará problemas al tiempo que se proponen las soluciones y alternativas
				legales que permitan superarlos.</li>
				<li>Distinguir cuándo una problemática tiene una solución legal y cuando se deben
				utilizar esquemas explicativos y soluciones de otras ramas del saber.</li>

            </div>
            
            <div id="Lista4" class="Elementos">
               <h2 class="text-center">PERFIL DE INGRESO</h2>
               <p>La Licenciatura en Intervención Educativa, se ofrece a egresados del nivel medio superior, a profesores en servicio y  a profesionistas interesados en cursar una nueva carrera.</p>
               <p>Los antecedentes académicos que se espera que posean los  aspirantes son: </p>
               <p>Conocimientos y habilidades básicas en Filosofía, Historia, Lógica, Matemáticas, lectura, expresión escrita y solución de problemas. </p>
               <p>Es importante que los aspirantes demuestren interés, habilidades y actitudes para el trabajo socioeducativo y psicopedagógico, y a su vez, cuenten con tiempo completo o realicen actividades con horarios flexibles.</p>
   
            </div>
            
