               <h2 class="text-center">DESCRIPCIÓN</h2>
               <p>La Licenciatura en Intervención educativa, responde a la intención de que los futuros profesionales puedan desempeñarse en distintos campos educativos con proyectos alternativos para solucionar problemas diversos. </p>
               <p>Los elementos teóricos y  metodológicos le permitirán al egresado adquirir las competencias que le permitan intervenir en problemas psicopedagógicos y socioeducativos.</p>
               <p>El modelo curricular de la Licenciatura en Intervención Educativa, comprende asignaturas agrupadas en tres áreas de formación:</p>
               <li>El área de Formación Inicial en Ciencias Sociales, comprende asignaturas obligatorias en el primero y segundo semestres, las cuales, permitirán la formación integral del sujeto en el área del conocimiento social y en lo diversos contextos culturales.</li>
               <li>El área de Formación Básica en Educación, contiene  asignaturas de iniciación al campo de la educación; comprende competencias genéricas o transversales que describen desempeños comunes a distintas ocupaciones y ramas de la actividad profesional. <br>
               Estas competencias tienen la finalidad de desarrollar en el futuro profesional las capacidades de adaptación, transferencia, resolución de problemas, flexibilidad de pensamiento, trabajo,  realización, control autónomo y sensibilidad social. </li>
               <li>El área de formación en líneas específicas, incluye  asignaturas a partir del tercer semestre; tiene como propósito facilitar  a los estudiantes una profundización en campos delimitados, que les permitan desempeñarse e intervenir con mayores elementos conceptuales, metodológicos y técnico-instrumentales en uno de los campos problemáticos específicos de educación, de acuerdo a las líneas específicas (Educación Inicial, Orientación Educacional, Educación Inclusiva, Educación de las Personas Jóvenes y Adultas, Interculturalidad, Gestión Educativa).</li><br>
               
               <p><strong>Cursos optativos:</strong> Los estudiantes cursarán cinco asignaturas optativas que fortalecerán su formación en las líneas específicas o en el desarrollo de competencias que se consideren convenientes.</p>
               <p><strong>Prácticas profesionales:</strong> Se realizarán durante los semestres 6º. 7º. y 8º. En los diversos campos de desempeño profesional, de acuerdo a las líneas de formación específica.  </p>
               <p><strong>Servicio social:</strong> Constituye un campo de formación práctica donde los estudiantes se integran a las problemáticas sociales a la vez que completan su aprendizaje; aporta nuevos enfoques a las prácticas y contribuye, con su trabajo, al mejoramiento social y comunitario. Se podrá realizar cuando los estudiantes hayan cursado un 70 % del  mapa curricular o de los créditos establecidos. </p>

